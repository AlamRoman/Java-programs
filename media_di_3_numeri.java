public class media_di_3_numeri{
    public static void main(String[] args){
        //inizializzazione dei variabili
        int a,b,c;
        float media;

        //dichiarazione dei variabili
        a = 5;
        b = 10;
        c = 20;

        //stampo i valori dei variabili
        System.out.println("a = " + a );
        System.out.println("b = " + b );
        System.out.println("c = " + c );

        //calcolo la media
        media = (float)(a+b+c)/3;

        //stampo la media
        System.out.println("\nLa media di numeri e': " + media );
    }
}